class AssignmentOperators{
	public static void main(String args[]){
		int x=10;
		System.out.println("x="+x);
		
		x+=5;
		System.out.println("x="+x);
		
		x-=2;
		System.out.println("x="+x);
		
		x*=3;
		System.out.println("x="+x);
		
		x/=4;
		System.out.println("x="+x);
		
		x%=5;
		System.out.println("x="+x);
	}
}