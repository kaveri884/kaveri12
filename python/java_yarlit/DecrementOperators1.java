class DecrementOperators1{
	public static void main(String args[]){
		int y=15;
		System.out.println(y);
		
		y=y-5;
		System.out.println(y);
		
		y-=5;
		System.out.println(y);
		
		y--;
		System.out.println(y);
		
		--y;
		System.out.println(y);
		
		
		System.out.println(y--);
		System.out.println(y);
		System.out.println(--y);
		
		
	}
}